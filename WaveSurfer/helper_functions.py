# For Python 3.0 and later
from urllib.request import urlopen
import certifi
import json
import numpy as np

def get_jsonparsed_data(url):
    response = urlopen(url, cafile=certifi.where())
    data = response.read().decode("utf-8")
    return json.loads(data)


